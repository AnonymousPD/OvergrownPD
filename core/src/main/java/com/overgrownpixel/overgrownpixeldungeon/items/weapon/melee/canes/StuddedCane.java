package com.overgrownpixel.overgrownpixeldungeon.items.weapon.melee.canes;

import com.overgrownpixel.overgrownpixeldungeon.items.weapon.melee.MeleeWeapon;
import com.overgrownpixel.overgrownpixeldungeon.sprites.items.ItemSpriteSheet;

public class StuddedCane extends MeleeWeapon {

	{
		image = ItemSpriteSheet.STUDDED_CANE;

		tier = 1;
        DLY = 0.25f; //4x speed
	}

	@Override
	public int max(int lvl) {
        return  Math.round(2f*(tier+1)) +     //4 base, down from 20
                lvl*Math.round(1f*(tier));  //+1 per level, down from +4
	}

}
