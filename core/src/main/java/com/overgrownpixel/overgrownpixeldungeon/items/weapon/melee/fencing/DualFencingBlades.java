package com.overgrownpixel.overgrownpixeldungeon.items.weapon.melee.fencing;

import com.overgrownpixel.overgrownpixeldungeon.items.weapon.melee.MeleeWeapon;
import com.overgrownpixel.overgrownpixeldungeon.sprites.items.ItemSpriteSheet;

public class DualFencingBlades extends MeleeWeapon {

	{
		image = ItemSpriteSheet.DUAL_FENCING_BLADES;

		tier = 2;
		DLY = 0.125f; //x8 speed
        ACC = 2f;     //+100% accuracy
	}

	@Override
	public int max(int lvl) {
        return  4*(tier) +      //8 base
                lvl*(tier);     //+2 per upgrade
	}

}
